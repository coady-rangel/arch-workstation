#!/usr/bin/env bash

################################################################################
# Common Helper Functions
################################################################################

# -----------------------------------------------------------------------------
# Logging
# -----------------------------------------------------------------------------

log_info() {
    echo "[INFO]  $*"
}

log_warn() {
    echo "[WARN]  $*"
}

log_error() {
    echo "[ERROR] $*" >&2
}

# -----------------------------------------------------------------------------
# Directories
# -----------------------------------------------------------------------------

create_collector_dir() {
    local output_dir="$1"
    local collector="$2"

    local dir="${output_dir}/${collector}"
    mkdir -p "${dir}"
    printf '%s\n' "${dir}"
}

# -----------------------------------------------------------------------------
# Commands
# -----------------------------------------------------------------------------

command_exists() {
    command -v "$1" >/dev/null 2>&1
}

capture_command() {
    local outfile="$1"
    shift

    "$@" > "${outfile}" 2>&1 || true
}

################################################################################
# Collector Status
################################################################################

write_status() {
    local collector_dir="$1"
    local status="$2"

    shift 2

    {
        echo "Status: ${status}"
        echo
        for line in "$@"; do
            echo "${line}"
        done
    } > "${collector_dir}/status.txt"
}
